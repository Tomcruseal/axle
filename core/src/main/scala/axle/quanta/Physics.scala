package axle.quanta

/**
 * The "Physics" objects models the graph of units
 * 
 * See http://en.wikipedia.org/wiki/SI_derived_unit
 * 
 */

object Physics {

  // The graph of quanta

  // Basic:
  //
  // mass
  // distance
  // information
  // time

  // Derived:
  //
  // area = distance * distance
  // volume = distance * distance * distance
  // speed = distance / time
  // acceleration = speed / time
  // force = mass * acceleration
  // energy = power * time
  // power = energy / time
  // flow = volume / time

}